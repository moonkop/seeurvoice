function convertToFreqMap(arr){
    let outArr = [];
    for (let i = 1; i < 300; i++) {
        let j = 1;
        let sum = 0;
        for (j = 1; ; j++) {
            if (arr[j * i]===undefined) {
                break;
            }
            sum += arr[j * i];
        }
        outArr.push(sum / j);
    }
    return outArr;
}

